pub const std = @import("std");

pub const Colour = struct {
    r: u8,
    g: u8,
    b: u8,
    a: u8,
};

pub const ColourList = struct {
    name: []const u8,
    colour: Colour,
};
pub fn rgb(r: u8, g: u8, b: u8) Colour {
    return .{ .r = r, .g = g, .b = b, .a = 255 };
}

pub fn getColour(name: []const u8) ?Colour {
    for (colours) |c| {
        if (std.mem.eql(u8, name, c.name)) return c.colour;
    }
    return null;
}

